void printReport(const char * lab[],  clock_t start[], clock_t end[], int n ) {

    float clks, sec, min;
    printf("\n\n\t%25s %17s\n\n", "Test Case", "Time Spent");

    for ( int i = 0; i < n ; i++ ) {
	clks =  (float) ( end[i]-start[i]); sec = clks / CLOCKS_PER_SEC; min = sec / 60;
	printf("\t%25s %10.0f clocks %8.2f sec %8.2f min\n", lab[i], clks, sec, min); 
    }

    printf("\n\n\t%25s %14s\n\n", "Case Comparison", "Times");
    for ( int i = 1; i < 3; i ++ ) 
	printf("\t\t%s / %s\t   %-15.2f\n", lab[i], lab[0],
			((float) end[i]-start[i])/((float) end[0]-start[0]));

    printf("\n\t\t------------- End of Test ---------------\n\n");
}

